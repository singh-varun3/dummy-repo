    private void removeUserjButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeUserjButtonActionPerformed
        // TODO add your handling code here:
        int row = userAccountJTable.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(null, "Please select a user account from table");
            return;
        }
        Organization org = null;
        
        UserAccount ua = (UserAccount)userAccountJTable.getValueAt(row, 2);
        
        
        if(userAccountJTable.getValueAt(row, 1) instanceof String)
        {
            enterprise.getUserAccountDirectory().removeUserAccount(ua);
        }
        else
        { org = (Organization) userAccountJTable.getValueAt(row, 1);
        
        org.getUserAccountDirectory().removeUserAccount(ua);
        }
        populateTable();
        
    }//GEN-LAST:event_removeUserjButtonActionPerformed

