    protected void populateTable(){
        DefaultTableModel model = (DefaultTableModel) userAccountJTable.getModel();

        model.setRowCount(0);
        
            for (UserAccount ua : enterprise.getUserAccountDirectory().getUserAccountList()) {
                Object row[] = new Object[5];
                row[0] = ua.getPerson().getFirstName()+ " "+ua.getPerson().getLastName();
                row[1] = "Enterprise Admin";
                row[2] = ua;
                row[3] = ua.getPassword();
                row[4] = ua.getRole();
                
                ((DefaultTableModel) userAccountJTable.getModel()).addRow(row);
            }
        
        

        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
            for (UserAccount ua : organization.getUserAccountDirectory().getUserAccountList()) {
                Object row[] = new Object[5];
                row[0] = ua.getPerson().getFirstName()+ " "+ua.getPerson().getLastName();
                if(organization instanceof SatelliteClinicOrganization)
            {
                SatelliteClinicOrganization satOrg =(SatelliteClinicOrganization)organization;
                row[1] = satOrg.getSatelliteClincName()+" Satellite Clinic";
            }
                else row[1] = organization;
                row[2] = ua;
                row[3] = ua.getPassword();
                row[4] = ua.getRole();
                
                ((DefaultTableModel) userAccountJTable.getModel()).addRow(row);
            }
        }
        
        
    }
    
    
