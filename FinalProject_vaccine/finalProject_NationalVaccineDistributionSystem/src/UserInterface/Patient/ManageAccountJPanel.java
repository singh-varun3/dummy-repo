    private void updatejButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatejButtonActionPerformed
        // TODO add your handling code here:
        try{
              Patient patient = (Patient)userAccount.getPerson();
            Double balance = Double.parseDouble(balancejTextField.getText());
            if(insurerjComboBox.isVisible() && coveredAmountjTextField.isVisible())
            { Double coveredAmount = Double.parseDouble(updatedCoveredAmountjTextField.getText());
        Insurance company = (Insurance)insurerjComboBox.getSelectedItem();
            patient.getPatientFinancialAccount().setInsurance(company);
            patient.getPatientFinancialAccount().setCoveredAmount(coveredAmount);
            }
            
        
        patient.getPatientFinancialAccount().setTotalBalance(balance);
        
        populatetheTextFields();
          JOptionPane.showMessageDialog(null, "Account Updated");
           
        }
        catch (NumberFormatException e)
        {
            JOptionPane.showMessageDialog(null, "Please enter numeric values");
            return;
        }
        
        
        
    }//GEN-LAST:event_updatejButtonActionPerformed

