    private void populatetheTextFields(){
        
        Patient patient = (Patient)userAccount.getPerson();
        if(patient.getPatientFinancialAccount() != null)
        {
            currentBalancejTextField.setText(String.valueOf(patient.getPatientFinancialAccount().getTotalBalance()));
            if(patient.getPatientFinancialAccount().getInsurance() != null)
            {
                insurerjTextField.setText(String.valueOf(patient.getPatientFinancialAccount().getInsurance().getName()));
                coveredAmountjTextField.setText(String.valueOf(patient.getPatientFinancialAccount().getCoveredAmount()));
                
            }
            else
            {
               insurerjTextField.setText("Not Insured");
                coveredAmountjTextField.setText("Not Insured"); 
            }
            
        }
        
    }
    
     private void populateInsurerJCombo(){
        
        insurerjComboBox.removeAllItems();
        
        for(Insurance company: business.getInsuranceCompanyList())
        {
            if(company != null)
            insurerjComboBox.addItem(company);
        }
        
    }

